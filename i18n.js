function getMessage(lang, key) {
  const base = i18nMessages.en;
  const current = i18nMessages[lang] || base;
  const parts = key.split(".");

  let value = current;
  for (let i = 0; i < parts.length; i += 1) {
    value = value && value[parts[i]];
  }
  if (typeof value === "string") return value;

  value = base;
  for (let i = 0; i < parts.length; i += 1) {
    value = value && value[parts[i]];
  }
  if (typeof value === "string") return value;

  return null;
}

function applyTranslations(lang) {
  document.documentElement.lang = lang;

  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (!key) return;
    const value = getMessage(lang, key);
    if (typeof value === "string") {
      el.textContent = value;
    }
  });

  const placeholderElements = document.querySelectorAll("[data-i18n-placeholder]");
  placeholderElements.forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (!key) return;
    const value = getMessage(lang, key);
    if (typeof value === "string") {
      el.setAttribute("placeholder", value);
    }
  });
}

function detectInitialLanguage() {
  const saved = window.localStorage.getItem("mu_lang");
  if (saved && i18nMessages[saved]) return saved;
  const browser = navigator.language || navigator.userLanguage || "en";
  const code = browser.toLowerCase().slice(0, 2);
  if (i18nMessages[code]) return code;
  return "en";
}

function initI18n() {
  const initialLang = detectInitialLanguage();
  const select = document.querySelector(".lang-switcher");
  if (select) {
    select.value = initialLang;
    select.addEventListener("change", () => {
      const value = select.value;
      window.localStorage.setItem("mu_lang", value);
      applyTranslations(value);
    });
  }
  applyTranslations(initialLang);
}

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", initI18n);
} else {
  initI18n();
}
